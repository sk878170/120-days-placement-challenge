# As we know about the data types of python, lets now learn about type casting
# Type casting means to change the data type

x = 7 # int
print(type(x))
print(x)

x = int(2.8) # int
print(type(x))
print(x)

x = int("3") # int
print(type(x))
print(x)

x= 1.2 # float
print(type(x))
print(x)

x = float(3) # float
print(type(x))
print(x)

x = float("4") # float
print(type(x))
print(x)

x= 'hello'
print(type(x)) # string
print(x)

x = str(3) # string
print(type(x))
print(x)

x = str(7.15) # string
print(type(x))
print(x)